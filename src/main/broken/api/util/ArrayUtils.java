package gregtech.api.util;

import java.util.Random;

public class ArrayUtils {

    public static void shuffle(Random random, int[] array) {
        int index, temp;
        for (int i = array.length - 1; i > 0; i--) {
            index = random.nextInt(i + 1);
            temp = array[index];
            array[index] = array[i];
            array[i] = temp;
        }
    }
}
